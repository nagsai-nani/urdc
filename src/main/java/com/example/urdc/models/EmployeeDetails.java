package com.example.urdc.models;

public class EmployeeDetails {
	private String empSalary;
	private String empTransport;
	private String empId;
	private String empContactNumber;
	private String empMartialStatus;
	private String empDOB;
	private String empTax;
	
	
	public String getEmpSalary() {
		return empSalary;
	}
	public void setEmpSalary(String empSalary) {
		this.empSalary = empSalary;
	}
	public String getEmpTransport() {
		return empTransport;
	}
	public void setEmpTransport(String empTransport) {
		this.empTransport = empTransport;
	}
	public String getEmpId() {
		return empId;
	}
	public void setEmpId(String empId) {
		this.empId = empId;
	}
	public String getEmpContactNumber() {
		return empContactNumber;
	}
	public void setEmpContactNumber(String empContactNumber) {
		this.empContactNumber = empContactNumber;
	}
	public String getEmpMartialStatus() {
		return empMartialStatus;
	}
	public void setEmpMartialStatus(String empMartialStatus) {
		this.empMartialStatus = empMartialStatus;
	}
	public String getEmpDOB() {
		return empDOB;
	}
	public void setEmpDOB(String empDOB) {
		this.empDOB = empDOB;
	}
	public String getEmpTax() {
		return empTax;
	}
	public void setEmpTax(String empTax) {
		this.empTax = empTax;
	}
		
}
